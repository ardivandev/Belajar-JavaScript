// * IF-ELSE, IF-ELSE IF
// digunakan untuk membuat keputusan berdasarkan kondisi tertentu.

// If-else
let age = 18;
if (age >= 18) {
  console.log("boleh vote");
} else {
  console.log("tidak boleh vote");
}

// if-else if
let nilai = 70;
if (nilai >= 90) {
  console.log("Grade : A");
} else if (nilai >= 80) {
  console.log("Grade : B");
} else {
  console.log("Grade : C");
}

// nested if-else
let num = 10;

if (num > 2) {
  if (num % 2 === 0) {
    console.log("Bilangan Genap");
  } else {
    console.log("Bilangan Ganjil");
  }
} else {
  console.log("Masukkan angka positive");
}

