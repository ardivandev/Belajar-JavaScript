// * Math Object
// object bawaan yang berisi properti dan metode untuk melakukan operasi matematika

// * Properti
console.log(Math.PI);
console.log(Math.E);

// * Metode Math
console.log(Math.abs(-10)); // 10
console.log(Math.pow(10, 2)); // 100
console.log(Math.sqrt(25)); // 5 (akar pangkat 2)
console.log(Math.cbrt(27)); // 3 (akar pangkat 3)
console.log(Math.max(1, 3, 4, 6, 8, 10, 11)); // mencari nilai tertinggi
console.log(Math.min(1, 3, 4, 6, 8, 10, 11)); // mencari nilai terendah

// * Pembulatan Angka
console.log(Math.round(3.6)); // ke terdekat
console.log(Math.floor(3.9)); // ke terkecil
console.log(Math.ceil(3.3)); // ke atas
console.log(Math.trunc(4.6)); // menghapus semua angka yang di belakang koma

// * Random Number
console.log(Math.random()); // antara 0 - 1
console.log(Math.round(Math.random() * 100));

