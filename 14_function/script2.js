// * Arrow Functions
// cara singkat untuk menulis function expression, diperkenalkan di ES6

// TODO : Jika function hanya memiliki 1 pernyataa. Anda bisa menghilangkan tanda kurung {} dan kata kunci return
let kali = (a, b) => a * b; // contohnya seperti ini
let bagi = (a, b) => {
  return a / b;
};
console.log(kali(10, 2));
console.log(bagi(10, 2));
